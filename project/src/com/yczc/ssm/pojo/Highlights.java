package com.yczc.ssm.pojo;

public class Highlights {

	private String id;
	private String title;
	private String picture;
	private String video;
	private String picadress;
	private String vidadress;
	private String time;
	private String introduction;
	
	private String author;
	private int view;
	private int priority;
	private String figure;
	
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public int getView() {
		return view;
	}
	public void setView(int view) {
		this.view = view;
	}
	public int getPriority() {
		return priority;
	}
	public void setPriority(int priority) {
		this.priority = priority;
	}
	public String getFigure() {
		return figure;
	}
	public void setFigure(String figure) {
		this.figure = figure;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPicadress() {
		return picadress;
	}
	public void setPicadress(String picadress) {
		this.picadress = picadress;
	}
	public String getVidadress() {
		return vidadress;
	}
	public void setVidadress(String vidadress) {
		this.vidadress = vidadress;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getVideo() {
		return video;
	}
	public void setVideo(String video) {
		this.video = video;
	}
	
}
